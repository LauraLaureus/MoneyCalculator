package moneycalculator.model;

public class Currency {
    
        private final String code;
        private final String name;
        private final String symbol;

    public Currency() {
        this.code = null;
        this.name = null;
        this.symbol = null;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getSymbol() {
        return symbol;
    }
        
     
    
}
